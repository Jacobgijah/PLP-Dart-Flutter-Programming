void main(){
    // Integer - int, stores the whole number
    int age;
    age = 25;

    // Double - double, stores floating-point number
    double temperature = 29.5;

    // String stores text
    String names = "Khalid Massawe";

    // List represents order collections of objects
    List<String> courses = ["Dart", "Python", "Web Development", "Database"];

    // Map stores collection of key-value pairs
    Map<String, dynamic> person = {
        'name': 'Collins',
        'age': 30,
        'isMarried': false,
    };

    // Printing values to demonstrate the use of different data types
    print('Integer: ${age}');
    print('Double: ${temperature}');
    print('String: ${names}');
    print('List: ${courses}');
    print('Map: ${person}');

}